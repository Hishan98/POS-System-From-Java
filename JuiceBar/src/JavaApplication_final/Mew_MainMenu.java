    private void btn_add_cartMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_add_cartMousePressed
        // TODO add your handling code here:
        Connection con = null;
        SQlConnection a= new SQlConnection();
        Calendar calendar=Calendar.getInstance();
        java.sql.Date startDate=new java.sql.Date(calendar.getTime().getTime());
        try{
                con = a.connect();
                String sql="Select * from products where itemcode=?";
                PreparedStatement pst=con.prepareStatement(sql);
                pst.setString(1,txt_itemcode.getText());
                ResultSet rs=pst.executeQuery();
                if(rs.next()){
                    
                    
                    String itemname=rs.getString("item");
                    int itemprice=rs.getInt("price");
                    String itemcode=rs.getString("itemcode"); 
                    String price=Integer.toString(itemprice);
                    
                    
                    lbl_item_name.setText(itemname); 
                    lbl_item_price.setText(price);
                    
                    con = a.connect();
                    String summation="Select SUM(price) FROM report where date=?";
                    PreparedStatement querysum=con.prepareStatement(summation);
                    querysum.setDate(1,startDate);
                    //JOptionPane.showMessageDialog(null,summation); 
                    
                    

                   int tot=Integer.parseInt(lbl_total.getText());
                    int qty=Integer.parseInt(txt_quentity.getText());
                    int total=tot+qty*itemprice;
                    String Full_price=Integer.toString(total);
                    lbl_total.setText(Full_price); 
                    
                    int full_price=qty*itemprice;
                    
                    DefaultTableModel model =(DefaultTableModel) jTable2.getModel();
                    model.addRow(new Object[]{itemcode,itemname,qty,full_price});
                    
                    //update report
                    
                    
                    String query="insert into Report(date,item,quentity,price)"+"values(?,?,?,?)";
                    PreparedStatement pstquery=con.prepareStatement(query);
                    pstquery.setDate(1,startDate);
                    pstquery.setString(2,itemname);
                    pstquery.setInt(3,qty);
                    pstquery.setInt(4,full_price);
                    pstquery.execute();

                }
                else{
                    JOptionPane.showMessageDialog(null,"Item number Not Matched. Please Try Again");
                    txt_itemcode.setText("");
                    lbl_item_name.setText("");
                    lbl_item_price.setText("");
                    txt_quentity.setText("1");
                }
                a.con_close(con);

            }
        
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_btn_add_cartMousePressed

    private void btn_printMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_printMousePressed
       
        if(lbl_total.getText()=="0"){
            JOptionPane.showMessageDialog(null,"Your Cart is Emphty Please Buy Something");        
        }
        
        else{ 
            if(txt_discount.getText().equals("0")){
            String tot=lbl_total.getText();
            JOptionPane.showMessageDialog(null,"Your Total Bill is = "+tot+"\nThank you For dealing with us See You Soon");
            }
            
            else{
                int dis=Integer.parseInt(txt_discount.getText());
                double discount=(double)dis;
                int tot=Integer.parseInt(lbl_total.getText());
                double tott=(double)tot;
                double total=(tott-(tott/100.0)*discount);
                String price=Integer.toString((int) total);
                lbl_total.setText(price);
                JOptionPane.showMessageDialog(null,"Congratulations you just won a "+txt_discount.getText()+"% "+" Discount From Our Store ");
                JOptionPane.showMessageDialog(null,"Your Total Bill is: "+price+"/= \n"+" Thank you For dealing with us See You Soon");
                
                txt_itemcode.setText("");
                lbl_item_name.setText("");
                lbl_item_price.setText("");
                txt_quentity.setText("1");
            }
        }
    }//GEN-LAST:event_btn_printMousePressed

